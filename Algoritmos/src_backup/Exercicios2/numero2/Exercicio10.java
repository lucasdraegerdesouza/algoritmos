package Exercicios2.numero2;

public class Exercicio10 {
	public static void main(String[] args) {
		/*10)	Imprima duas listas, uma de 0 a 10 e outra de 10 a 0;*/
		int n1 = -1;
		int n2 = 11;
		while (n1 <= 10 & n2 >= 1){
		n1++;
		n2--;
		System.out.println(n1+ "          " +n2);
		}
	}
}

